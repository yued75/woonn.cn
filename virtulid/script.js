        const idQueryInput = document.getElementById('idQueryInput');
        if (idQueryInput) {
            idQueryInput.removeEventListener('keypress', function (e) {
                if (e.key === 'Enter') completeId();
            });
            idQueryInput.addEventListener('keypress', function (e) {
                if (e.key === 'Enter') completeId();
            });
        }
